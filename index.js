function add(a, b) {
    return a + b;
}
function subtract(a, b) {
    return a - b;
}
function multiply(a, b) {
    return a * b;
}
function divide(a, b) {
    return a / b;
}
function increment(number) {
    return number += 1;
}
function decrement(number) {
    return number -= 1;
}
function makeInt(a) {
    return parseInt(a, 10);
}
function makeInt(a = 2) {
    return parseInt(a, 10)
}
function preserveDecimal(a) {
    return parseFloat(a);
}
